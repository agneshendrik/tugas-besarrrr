    public void selectData(){
        String kolom[] = {"id", "kode_barang", "nama_barang"};
        DefaultTableModel dtm = new DefaultTableModel(null, kolom);
        String SQL = "SELECT * from tpos";
        ResultSet rs = koneksidatabase.executeQuery(SQL);
        
        try {
            while (rs.next()){
                String kode_barang = rs.getString(1);
                String nama_barang = rs.getString(2);
                String data[] = {kode_barang, nama_barang};
                dtm.addRow(data);
            }
        } catch (SQLException e) {
            Logger.getLogger(fpos.class.getName()).log(Level.SEVERE, null, e);
        }
        
    }

