    private void btnhargaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhargaActionPerformed
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/login" , "root", "");
            String sql = "Select * from tpos where kode_barang=?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, txtkode.getText());
            ResultSet rs = pst.executeQuery();
            if(rs.next()){
                int harga = Integer.parseInt(txtqty.getText()) * Integer.parseInt(rs.getString(5));
                txtharga.setText(Integer.toString(harga));
            } else {
                
            } con.close();
        } catch (Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_btnhargaActionPerformed

